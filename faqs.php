                         <h2>Product Registration Process</h2>

					<hr>

                        <div class="row">
						<div class="col">
                            <div class="toggle toggle-primary" data-plugin-toggle>
								<section class="toggle active">
									<label>How do I know the category my product belongs to?</label>
									<p>You can find out by checking the guidelines on NAFDAC website at <a href="https://www.nafdac.gov.ng/" target="_blank">https://www.nafdac.gov.ng/</a></p>
								</section>

								<section class="toggle">
									<label>What are the product categories available?</label>
									<p>The product categories include: Food, Cosmetics, Drug, Medical Device, Agro-Chemicals & Pesticide, Veterinary Products, Vaccines & Biologicals, Herbal and Nutraceuticals and Water.</p>
								</section>

								<section class="toggle">
									<label>How will I know the status of my application at any point in time?</label>
									<p>You will get a mail notification at every point in the registration process. You can also check the status of your application by login to your account on NAPAMS.</p>
								</section>

								<section class="toggle">
									<label>Do I have to physically drop off my product sample at NAFDAC office?</label>
									<p>No, you can choose to have it sent to NAFDAC office.</p>
								</section>

								<section class="toggle">
									<label>How will I know when I have a Compliance Directive?</label>
									<p>A mail notification is sent once a compliance directive is issued. You can also track compliance directive issued to you company from your account.</p>
								</section>

								<section class="toggle">
									<label>Can I process/register multiple products at the same time?</label>
									<p>Yes, you can process/register multiple products at the same time.</p>
								</section>

                            <section class="toggle">
									<label>Can I process my product in the zonal/state office?</label>
									<p>Yes, the registration process of some products can be completed in the zonal/state office.</p>
								</section>

                            <section class="toggle">
									<label>What are the products that can be completely registered in the zonal/state office?</label>
									<p>
                                        The list of products that can be registered in the zonal/state office are:
                                            o Bread and bread products
                                            o Packaged Water (bottle and sachet) renewal only
                                            o Dry finger foods – chin-chin, popcorn, plantain and potato chips
                                            o Locally grown rice and other packaged farm produce
                                            o Locally grown rice and other packaged farm produce
                                            o Palm oil
                                            o Dried Fish and fish products
                                            o Dried Meat and meat product
                                            o Liquid soaps (Hand wash, Car wash, Dish wash)
                                            o Petroleum jelly
									</p>
								</section>

                            <section class="toggle">
									<label>Can I choose to register any of the above products in Lagos?</label>
									<p>No, the registration process will can be completed in the zonal/state office.</p>
								</section>

                            <section class="toggle">
									<label>When can I start renewal of my product(s)?</label>
									<p>You can start renewal 6 months to the date of expiry.</p>
								</section>

                            <section class="toggle">
									<label>Is there a penalty for late renewal?</label>
									<p>Yes, there is a late renewal fee, which is dependent on the category of the product.</p>
								</section>

                            <section class="toggle">
									<label>Can I use a food form for a drug product (for example)?</label>
									<p>No, you have to use the form category your product belongs to.</p>
								</section>

                            <section class="toggle">
									<label>What happen if I buy the wrong category of forms?</label>
									<p>Nothing the purchased form will be in your account for future use.</p>
								</section>
